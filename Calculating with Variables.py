my_number = int(input("Please enter an integer: "))
print(my_number)
print("2 is added to the chosen number")
my_number = my_number + 2
print(my_number)
print("The result is multiplied by 3")
my_number = my_number * 3
print(my_number)
print("6 is subtracted to the result")
my_number = my_number - 6
print(my_number)
print("The result is divided by 3")
my_number = my_number / 3
print(my_number)
print("Surprise! The result is equal to the chosen number")
